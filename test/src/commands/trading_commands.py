    async def _create_signal_embed(self, signal: dict, user_tier: str) -> discord.Embed:
        """Cr√©ation de l'embed pour un signal"""
        action = signal['action']
        color_map = {
            'BUY': discord.Color.green(),
            'SELL': discord.Color.red(),
            'HOLD': discord.Color.yellow()
        }

        action_emoji = {
            'BUY': 'üöÄ',
            'SELL': 'üìâ',
            'HOLD': '‚è∏Ô∏è'
        }

        embed = discord.Embed(
            title=f"{action_emoji.get(action, 'üìä')} Signal {action} - {signal['symbol']}",
            color=color_map.get(action, discord.Color.blue()),
            timestamp=datetime.utcnow()
        )

        # Informations de base (tous niveaux)
        embed.add_field(name="üí∞ Prix Actuel", value=f"${signal['price']:.4f}", inline=True)
        embed.add_field(name="üìä Confiance", value=f"{signal['confidence']:.1f}%", inline=True)
        embed.add_field(name="‚è∞ Timeframe", value=signal['timeframe'], inline=True)

        # Informations Premium
        if user_tier in ['premium', 'vip'] and action != 'HOLD':
            if signal.get('take_profit'):
                embed.add_field(name="üéØ Take Profit", value=f"${signal['take_profit']:.4f}", inline=True)
            if signal.get('stop_loss'):
                embed.add_field(name="üõë Stop Loss", value=f"${signal['stop_loss']:.4f}", inline=True)
            if signal.get('risk_reward'):
                embed.add_field(name="üíé Risk/Reward", value=f"1:{signal['risk_reward']:.2f}", inline=True)

        # Informations VIP
        if user_tier == 'vip':
            indicators = signal.get('indicators', {})
            if indicators.get('rsi'):
                embed.add_field(name="üìà RSI", value=f"{indicators['rsi']:.1f}", inline=True)
            if indicators.get('macd'):
                macd_signal = "üü¢" if indicators['macd'] > indicators.get('macd_signal', 0) else "üî¥"
                embed.add_field(name="üìâ MACD", value=macd_signal, inline=True)

        # Recommandations
        recommendations = signal.get('recommendations', [])[:3]  # Limite √† 3
        if recommendations:
            embed.add_field(
                name="üí° Recommandations",
                value='\n'.join(recommendations),
                inline=False
            )

        # Footer selon le niveau
        tier_footer = {
            'free': "Niveau Gratuit - Upgrade pour plus de d√©tails!",
            'premium': "Niveau Premium - Analyses avanc√©es incluses",
            'vip': "Niveau VIP - Acc√®s complet aux analyses"
        }

        embed.set_footer(text=f"Trading Bot Premium ¬© 2025 | {tier_footer.get(user_tier, '')}")

        return embed

    async def _create_analysis_embed(self, symbol: str, analysis_1h: dict, analysis_4h: dict, analysis_1d: dict, user_tier: str) -> discord.Embed:
        """Cr√©ation de l'embed pour l'analyse compl√®te"""
        embed = discord.Embed(
            title=f"üìä Analyse Technique Compl√®te - {symbol}",
            description="Analyse multi-timeframes d√©taill√©e",
            color=discord.Color.blue(),
            timestamp=datetime.utcnow()
        )

        # Analyse par timeframe
        timeframes = [
            ("1H", analysis_1h),
            ("4H", analysis_4h),
            ("1D", analysis_1d)
        ]

        for tf_name, analysis in timeframes:
            if not analysis.get('error'):
                signal = analysis['signal']
                confidence = analysis['confidence']

                signal_emoji = {'BUY': 'üü¢', 'SELL': 'üî¥', 'HOLD': 'üü°'}

                embed.add_field(
                    name=f"‚è∞ {tf_name}",
                    value=f"{signal_emoji.get(signal, 'üü°')} {signal}\n{confidence:.1f}% confiance",
                    inline=True
                )

        # Indicateurs techniques (VIP uniquement)
        if user_tier == 'vip' and not analysis_1h.get('error'):
            indicators = analysis_1h.get('indicators', {})

            embed.add_field(
                name="üìà Indicateurs Techniques",
                value=f"RSI: {indicators.get('rsi', 0):.1f}\n"
                      f"MACD: {'üü¢' if indicators.get('macd', 0) > indicators.get('macd_signal', 0) else 'üî¥'}\n"
                      f"BB Position: {indicators.get('bb_position', 0.5):.2f}",
                inline=True
            )

        # Patterns d√©tect√©s
        if not analysis_1h.get('error'):
            patterns = analysis_1h.get('patterns', {})
            bullish = len(patterns.get('bullish_patterns', []))
            bearish = len(patterns.get('bearish_patterns', []))

            pattern_text = f"üü¢ Haussiers: {bullish}\nüî¥ Baissiers: {bearish}"

            embed.add_field(
                name="üîç Patterns D√©tect√©s",
                value=pattern_text,
                inline=True
            )

        embed.set_footer(text="Analyse Premium - Ne constitue pas un conseil financier")

        return embed

    async def _create_portfolio_embed(self, portfolio: list, user_tier: str) -> discord.Embed:
        """Cr√©ation de l'embed pour le portfolio"""
        embed = discord.Embed(
            title="üìä Votre Portfolio",
            description="Suivi de vos positions de trading",
            color=discord.Color.blue(),
            timestamp=datetime.utcnow()
        )

        total_value = 0
        total_pnl = 0

        for position in portfolio[:10]:  # Limite √† 10 positions
            symbol = position['symbol']
            quantity = position['quantity']
            entry_price = position['entry_price']
            current_price = position['current_price'] or entry_price

            position_value = quantity * current_price
            pnl = (current_price - entry_price) / entry_price * 100
            pnl_emoji = "üü¢" if pnl >= 0 else "üî¥"

            total_value += position_value
            total_pnl += pnl

            embed.add_field(
                name=f"{symbol}",
                value=f"Qty: {quantity:.4f}\n"
                      f"P&L: {pnl_emoji} {pnl:+.2f}%\n"
                      f"Valeur: ${position_value:.2f}",
                inline=True
            )

        # R√©sum√© global
        avg_pnl = total_pnl / len(portfolio) if portfolio else 0
        pnl_color = "üü¢" if avg_pnl >= 0 else "üî¥"

        embed.add_field(
            name="üíº R√©sum√© Global",
            value=f"Valeur Totale: ${total_value:.2f}\n"
                  f"P&L Moyen: {pnl_color} {avg_pnl:+.2f}%\n"
                  f"Positions: {len(portfolio)}",
            inline=False
        )

        embed.set_footer(text="Portfolio Premium - Mis √† jour en temps r√©el")

        return embed

